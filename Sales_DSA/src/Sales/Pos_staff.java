    public void Balance()
    {
        
        float total = Float.parseFloat(txtotal.getText());
        float pay = Float.parseFloat(txtpay.getText());
        
        float bal = (pay - total);
        txtbal.setText(String.valueOf(df.format(bal))); 
    
    
    }
    public void bill()
    {
        String total = txtotal.getText();
        String pay = txtpay.getText();
        String bal = txtbal.getText();
        
        DefaultTableModel model = new DefaultTableModel();
        model = (DefaultTableModel)jTable1.getModel();
        
        txtbill.setText(txtbill.getText() + "********************************************************************\n");
        txtbill.setText(txtbill.getText() + "                     BILL                                           \n");
        txtbill.setText(txtbill.getText() + "********************************************************************\n");
        
        //Heading
        txtbill.setText(txtbill.getText() + "Product" + "\t" + "Price" + "\t" + "Quantity" + "\t" + "Amount" + "\n");
        
        
        for(int i = 0; i < model.getRowCount(); i++)
        {
            String pname = (String)model.getValueAt(i, 1);
            String price = (String)model.getValueAt(i, 3);
            String amount = (String)model.getValueAt(i, 4);
            String quan = (String)model.getValueAt(i, 2);
            
            txtbill.setText(txtbill.getText() + pname + "\t" + price + "\t" + quan + "\t\t" + amount + "\n");
        }
        txtbill.setText(txtbill.getText() + "\n");
        txtbill.setText(txtbill.getText() + "\t" + "\t" + "Subtotal: " + total + "\n");
        txtbill.setText(txtbill.getText() + "\t" + "\t" + "Pay: " + pay + "\n");
        txtbill.setText(txtbill.getText() + "\t" + "\t" + "Balance: " + bal + "\n");
        txtbill.setText(txtbill.getText() + "\n");
        txtbill.setText(txtbill.getText() + "********************************************************************\n");
        txtbill.setText(txtbill.getText() + "             THANK YOU COME AGAIN                           \n");
    }
    
    
    
    
    
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:for(int i = 0; i < tblModel.getRowCount(); i++) 
            String procode = txtpcode.getText();
            String quant = txtqty.getValue().toString();
            int quant1 = Integer.parseInt(quant);
            if(quant1>0)
            {
            try{
                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost/salespos","root","");
                Statement st = con.createStatement();
                String query = "select * from product where id='" +procode+"'";
                rs = st.executeQuery(query);
                while(rs.next()){
                    String stockss = rs.getString(4);
                    int stocks = Integer.parseInt(stockss);
                    if(stocks>=quant1){
                        DefaultTableModel model = new DefaultTableModel();
                        model = (DefaultTableModel)jTable1.getModel();
                        model.addRow(new Object[]
                
                        {
                            txtpcode.getText(),
                            txtpname.getText(),
                            txtqty.getValue().toString(),
                            txtprice.getText(),
                            txtamount.getText(),
                        });
                        txtqty.setValue(0);
                        float sum = 0;
                        for(int i = 0; i<jTable1.getRowCount(); i ++ )
                        {
                            sum = sum + Float.parseFloat(jTable1.getValueAt(i, 4).toString());
        
                        }
                        txtotal.setText(String.valueOf(df.format(sum)));
                            txtpcode.setText("");
                            txtpname.setText("");
                            txtprice.setText("");
                            txtamount.setText("");
            
                        txtpcode.requestFocus();
                }else{
                    JOptionPane.showMessageDialog(this, "Not enough stock: " + txtpname.getText());
                }
            }
            }
            catch(SQLException e)
            {
                JOptionPane.showMessageDialog(null, e);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Pos_staff.class.getName()).log(Level.SEVERE, null, ex);
            }
            }else{
                JOptionPane.showMessageDialog(null, "Quantity must be positive.");
            }
    }//GEN-LAST:event_jButton1ActionPerformed

